public class StatementsWhiteSpaceAndIndenting {
    public static void main(String[] args){
        //Expression - myVariable = 50;
        //Statement - int myVariable = 50;
        //Statement - myVariable++;

        //Java does get rid of whitespace when running the code,
        //but it is best practice to include spaces for readability

        //Indenting is used to specify code blocks for logical flow and readability

        int myVariable = 50;
        myVariable++;
        myVariable--;
        System.out.println("This is a test");


    }
}
